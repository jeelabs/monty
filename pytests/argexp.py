def f(*a):
    print("f", a)

f()
f(1)
f(1,2)
f(1,2,3,*range(5, 10, 2))
f(1,2,3,4,*(55+i for i in range(4)))

def l(a,b):
    print("l",a,b)

l(*[1,2])
l(11,*[22])
l(**{"b":222,"a":111})
l(b=222,**{"a":111})
l(*[22],**{"b":11})

def m(**k):
    print("m",k)

m()
m(x=1,y=2)
m(a=11,b=22,**{"c":33,"d":44})
m(**{"e":5,"f":6})
